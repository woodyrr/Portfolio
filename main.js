let bro = document.querySelector('.bro')
let yo = document.querySelector('.yo')
yo.addEventListener('mouseover',function dang(){
    
    bro.style.opacity = '100'
})
yo.addEventListener('click',function dang(){
    
    bro.style.opacity = '100'
})

yo.addEventListener('mouseout',function(){
    bro.style.opacity = '0'
})
yo.addEventListener('ontouchend',function dang(){
    
    bro.style.opacity = '0'
})


